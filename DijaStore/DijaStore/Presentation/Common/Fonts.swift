import UIKit

extension UIFont {
    enum App {
        static let subtitle = UIFont.systemFont(ofSize: 18)
        static let secondarySubtitle = UIFont.systemFont(ofSize: 14, weight: .medium)
        static let body = UIFont.systemFont(ofSize: 16)
        static let secondaryBody = UIFont.systemFont(ofSize: 14)
        static let control = UIFont.systemFont(ofSize: 14, weight: .medium)
        static let caption = UIFont.systemFont(ofSize: 12)
        static let controlCaption = UIFont.systemFont(ofSize: 12, weight: .medium)
    }
}
